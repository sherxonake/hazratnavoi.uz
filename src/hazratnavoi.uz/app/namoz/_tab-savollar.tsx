function QACard({ qa, idx, open, onToggle }: { qa: QA; idx: number; open: boolean; onToggle: () => void }) {
  return (
    <div className={cn("rounded-2xl border transition-all overflow-hidden",
      open ? "border-yellow-400/50 bg-card" : "border-[var(--border)] bg-card/60 hover:border-yellow-400/30")}>
      <button className="w-full flex items-center gap-3 px-5 py-4 text-left" onClick={onToggle}>
        <div className={cn("flex-shrink-0 w-8 h-8 rounded-full flex items-center justify-center text-sm font-bold transition-colors",
          open ? "bg-yellow-400/20 text-yellow-300 border border-yellow-400/50" : "bg-white/5 text-muted-foreground border border-white/10")}>
          {idx + 1}
        </div>
        <div className="flex-1 min-w-0">
          <span className={cn("font-semibold text-sm leading-snug", open ? "text-yellow-300" : "text-foreground")}>
            {qa.q}
          </span>
        </div>
        {open
          ? <ChevronUp className="w-4 h-4 text-yellow-400 flex-shrink-0" />
          : <ChevronDown className="w-4 h-4 text-muted-foreground flex-shrink-0" />}
      </button>
      {open && (
        <div className="border-t border-[var(--border)] px-5 pb-4 pt-3">
          <p className="text-foreground/70 text-sm leading-relaxed">{qa.a}</p>
        </div>
      )}
    </div>
  )
}

export function TabSavollar() {
  const [openIdx, setOpenIdx] = useState<number | null>(null)
  const [cat, setCat] = useState<"tahorat" | "namoz">("tahorat")

  const filtered = SAVOLLAR.filter(q => q.cat === cat)
  const tahoratCount = SAVOLLAR.filter(q => q.cat === "tahorat").length
  const namozCount = SAVOLLAR.filter(q => q.cat === "namoz").length

  function toggle(globalIdx: number) {
    setOpenIdx(prev => prev === globalIdx ? null : globalIdx)
  }

  return (
    <div className="space-y-3">
      {/* Category tabs */}
      <div className="flex gap-2 mb-2">
        <button
          onClick={() => { setCat("tahorat"); setOpenIdx(null) }}
          className={cn("flex-1 py-2.5 rounded-xl text-sm font-semibold border transition-all",
            cat === "tahorat"
              ? "bg-emerald-500/20 border-emerald-500/50 text-emerald-300"
              : "bg-white/5 border-white/10 text-muted-foreground hover:border-emerald-500/30")}>
          💧 Таҳорат
          <span className="ml-1.5 text-xs opacity-70">({tahoratCount})</span>
        </button>
        <button
          onClick={() => { setCat("namoz"); setOpenIdx(null) }}
          className={cn("flex-1 py-2.5 rounded-xl text-sm font-semibold border transition-all",
            cat === "namoz"
              ? "bg-emerald-500/20 border-emerald-500/50 text-emerald-300"
              : "bg-white/5 border-white/10 text-muted-foreground hover:border-emerald-500/30")}>
          🕌 Намоз
          <span className="ml-1.5 text-xs opacity-70">({namozCount})</span>
        </button>
      </div>

      {/* Q&A list */}
      {filtered.map((qa, i) => {
        const globalIdx = SAVOLLAR.indexOf(qa)
        return (
          <QACard
            key={globalIdx}
            qa={qa}
            idx={i}
            open={openIdx === globalIdx}
            onToggle={() => toggle(globalIdx)}
          />
        )
      })}

      {/* Bottom note */}
      <div className="mt-4 rounded-2xl bg-emerald-500/10 border border-emerald-500/20 p-4">
        <div className="flex gap-3 items-start">
          <div className="text-xl">📚</div>
          <div>
            <div className="text-emerald-300 text-sm font-semibold mb-1">Манба</div>
            <div className="text-foreground/60 text-xs leading-relaxed">
              Саволлар ва жавоблар <span className="text-emerald-400">namoz.islom.uz</span> сайтидан олинган. Ҳанафий мазҳабига асосланган.
            </div>
          </div>
        </div>
      </div>
    </div>
  )
}
